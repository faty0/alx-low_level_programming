#include <stdio.h>
/**
 * add - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the addition of 2 numbers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the substraction of 2 numbers
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the multiplication of 2 numbers
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the division of 2 numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - the modulu two numbers
 * @a: first number
 * @b: second number
 *
 * Return: the modulu of 2 numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
